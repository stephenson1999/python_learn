def partition(A, low , high):
    piovot = A[high]

    








    






































    i = low - 1









































    for j in range(low, high):





































        if A[j] <= piovot:














































            i = i + 1
            (A[i], A[j]) = (A[j], A[i])
    (A[i+1], A[high]) = (A[high], A[i+1])

    return i + 1
















































































































































def quicksort(A, low , high):
    if low > high:
        pi = partition(A,low,high)
        quicksort(A,low,pi-1)
        quicksort(A,pi + 1, high)

